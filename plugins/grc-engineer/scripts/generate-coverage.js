#!/usr/bin/env node

/**
 * Generate docs/FRAMEWORK-COVERAGE.md from the live state of the repo.
 *
 * Produces the canonical status page for "which of the 249 SCF-mapped
 * frameworks have dedicated plugins." Intended to run on every release
 * (or as a weekly cron) so the doc never drifts from reality.
 *
 * Usage:
 *   node scripts/generate-coverage.js [--out=<path>] [--offline]
 *
 * Defaults to writing docs/FRAMEWORK-COVERAGE.md at repo root.
 */

import fs from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';
import { initSCF } from './scf-client.js';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const REPO_ROOT = path.resolve(__dirname, '../../..');
const FRAMEWORKS_DIR = path.join(REPO_ROOT, 'plugins', 'frameworks');
const DEFAULT_OUT = path.join(REPO_ROOT, 'docs', 'FRAMEWORK-COVERAGE.md');

function parseArgs(argv) {
  const args = argv.slice(2);
  const opts = { out: DEFAULT_OUT, offline: false };
  for (const a of args) {
    if (a.startsWith('--out=')) opts.out = path.resolve(a.slice('--out='.length));
    else if (a === '--offline') opts.offline = true;
    else if (a === '--help' || a === '-h') opts.help = true;
  }
  return opts;
}

function regionFromFrameworkId(frameworkId) {
  if (frameworkId.startsWith('americas-') || frameworkId.startsWith('amaericas-') || frameworkId.startsWith('usa-')) return 'Americas';
  if (frameworkId.startsWith('apac-')) return 'APAC';
  if (frameworkId.startsWith('emea-')) return 'EMEA';
  return 'Global';
}

async function loadShippedPlugins() {
  const entries = await fs.readdir(FRAMEWORKS_DIR, { withFileTypes: true });
  const plugins = new Map();  // scf_framework_id → { slug, depth, display_name }
  const unmapped = [];        // [{slug, description}]
  for (const e of entries) {
    if (!e.isDirectory()) continue;
    const pluginFile = path.join(FRAMEWORKS_DIR, e.name, '.claude-plugin', 'plugin.json');
    try {
      const data = JSON.parse(await fs.readFile(pluginFile, 'utf8'));
      const fwMeta = data.framework_metadata;
      if (fwMeta?.scf_framework_id) {
        plugins.set(fwMeta.scf_framework_id, {
          slug: e.name,
          depth: fwMeta.depth || 'unknown',
          display_name: fwMeta.display_name || data.description || e.name,
        });
      } else {
        unmapped.push({ slug: e.name, description: data.description || '' });
      }
    } catch {
      // ignore unreadable plugins
    }
  }
  return { plugins, unmapped };
}

function render({ scfFrameworks, shipped, generatedAt }) {
  const { plugins, unmapped } = shipped;
  const lines = [];

  lines.push('# Framework Coverage');
  lines.push('');
  lines.push('> **Auto-generated.** Regenerated by `node plugins/grc-engineer/scripts/generate-coverage.js`. Do not hand-edit — your changes will be overwritten on the next release.');
  lines.push('');
  lines.push(`Last generated: ${generatedAt.toISOString().slice(0, 10)}`);
  lines.push('');
  lines.push('This page tracks which of the 249 frameworks in the [Secure Controls Framework](https://securecontrolsframework.com) crosswalk have a dedicated plugin in this repo. Frameworks without a dedicated plugin are **already usable today** via `/grc-engineer:gap-assessment <scf-framework-id>` through the SCF crosswalk.');
  lines.push('');

  // Summary
  const totalScf = scfFrameworks.length;
  const shippedCount = plugins.size;
  const notStarted = totalScf - shippedCount;
  const pct = ((shippedCount / totalScf) * 100).toFixed(1);

  lines.push('## Summary');
  lines.push('');
  lines.push(`| | |`);
  lines.push(`|---|---|`);
  lines.push(`| Total SCF-mapped frameworks | ${totalScf} |`);
  lines.push(`| With dedicated plugin | **${shippedCount}** (${pct}%) |`);
  lines.push(`| Crosswalk-only (no plugin yet) | ${notStarted} |`);
  if (unmapped.length > 0) {
    lines.push(`| Shipped plugins without SCF mapping | ${unmapped.length} |`);
  }
  lines.push('');

  // Depth breakdown
  const depthCounts = { full: 0, reference: 0, stub: 0, unknown: 0 };
  for (const p of plugins.values()) depthCounts[p.depth] = (depthCounts[p.depth] || 0) + 1;
  lines.push('### By depth');
  lines.push('');
  lines.push('| Depth | Count | Description |');
  lines.push('|---|---|---|');
  lines.push(`| Full | ${depthCounts.full || 0} | Framework-native workflow commands + Reference content |`);
  lines.push(`| Reference | ${depthCounts.reference || 0} | Scope + evidence checklist + framework-specific SKILL |`);
  lines.push(`| Stub | ${depthCounts.stub || 0} | Scaffolded delegation to \`/grc-engineer:gap-assessment\` |`);
  if (depthCounts.unknown) {
    lines.push(`| Unknown | ${depthCounts.unknown} | Plugins without \`framework_metadata.depth\` — needs backfill |`);
  }
  lines.push('');
  lines.push('See [Framework Plugin Guide](FRAMEWORK-PLUGIN-GUIDE.md) for depth definitions and level-up checklists.');
  lines.push('');

  // Shipped plugins table
  lines.push('## Shipped plugins');
  lines.push('');
  if (plugins.size === 0) {
    lines.push('_None yet._');
  } else {
    lines.push('| Namespace | Depth | SCF framework ID | Display name |');
    lines.push('|---|---|---|---|');
    const shippedRows = [];
    for (const [scfId, p] of plugins.entries()) {
      const scfEntry = scfFrameworks.find(f => f.framework_id === scfId);
      const display = scfEntry?.display_name || p.display_name;
      shippedRows.push({ slug: p.slug, depth: p.depth, scfId, display, region: regionFromFrameworkId(scfId) });
    }
    shippedRows.sort((a, b) => a.slug.localeCompare(b.slug));
    for (const r of shippedRows) {
      lines.push(`| \`/${r.slug}:\` | ${r.depth} | \`${r.scfId}\` | ${r.display} |`);
    }
  }
  lines.push('');

  if (unmapped.length > 0) {
    lines.push('### Shipped without SCF mapping');
    lines.push('');
    lines.push('These plugins exist in the marketplace but do not have a `framework_metadata.scf_framework_id` in their `plugin.json`. Either no single SCF entry applies (e.g. the plugin spans multiple baselines), or the metadata block hasn\'t been backfilled yet. Contributions to add metadata are welcome.');
    lines.push('');
    lines.push('| Namespace | Description |');
    lines.push('|---|---|');
    unmapped.sort((a, b) => a.slug.localeCompare(b.slug));
    for (const u of unmapped) {
      const desc = u.description.split('—')[0].trim().slice(0, 100);
      lines.push(`| \`/${u.slug}:\` | ${desc} |`);
    }
    lines.push('');
  }

  // Not started, grouped by region
  const grouped = { Americas: [], APAC: [], EMEA: [], Global: [] };
  for (const f of scfFrameworks) {
    if (plugins.has(f.framework_id)) continue;
    grouped[regionFromFrameworkId(f.framework_id)].push(f);
  }
  for (const region of Object.keys(grouped)) {
    grouped[region].sort((a, b) => a.display_name.localeCompare(b.display_name));
  }

  lines.push('## Not started');
  lines.push('');
  lines.push(`${notStarted} frameworks are supported today via the SCF crosswalk but have no dedicated plugin. To adopt one:`);
  lines.push('');
  lines.push('```bash');
  lines.push('node plugins/grc-engineer/scripts/scaffold-framework.js <scf-framework-id>');
  lines.push('```');
  lines.push('');
  lines.push('Or keep using the crosswalk directly:');
  lines.push('');
  lines.push('```bash');
  lines.push('/grc-engineer:gap-assessment "<scf-framework-id>"');
  lines.push('```');
  lines.push('');

  for (const region of ['Americas', 'APAC', 'EMEA', 'Global']) {
    const rows = grouped[region];
    if (rows.length === 0) continue;
    lines.push(`### ${region} — ${rows.length} frameworks`);
    lines.push('');
    lines.push('<details><summary>Expand</summary>');
    lines.push('');
    lines.push('| SCF framework ID | Display name | SCF controls → framework controls |');
    lines.push('|---|---|---|');
    for (const r of rows) {
      lines.push(`| \`${r.framework_id}\` | ${r.display_name} | ${r.scf_controls_mapped} → ${r.framework_controls_mapped} |`);
    }
    lines.push('');
    lines.push('</details>');
    lines.push('');
  }

  return lines.join('\n');
}

async function main() {
  const opts = parseArgs(process.argv);
  if (opts.help) {
    console.log('Usage: node scripts/generate-coverage.js [--out=<path>] [--offline]');
    return;
  }

  const scf = await initSCF({ offline: opts.offline });
  const [scfFrameworks, shipped] = await Promise.all([
    scf.frameworks(),
    loadShippedPlugins(),
  ]);

  const md = render({ scfFrameworks, shipped, generatedAt: new Date() });
  await fs.writeFile(opts.out, md);
  console.log(`Wrote ${path.relative(REPO_ROOT, opts.out)}`);
  console.log(`  ${shipped.plugins.size} / ${scfFrameworks.length} frameworks with dedicated plugins`);
  if (shipped.unmapped.length > 0) {
    console.log(`  ${shipped.unmapped.length} shipped plugins without SCF mapping`);
  }
}

if (import.meta.url === pathToFileURL(process.argv[1]).href) {
  main().catch(err => {
    console.error('✗ generate-coverage:', err.message);
    if (process.env.DEBUG) console.error(err);
    process.exit(1);
  });
}
